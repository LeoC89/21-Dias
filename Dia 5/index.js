//Criando variável
let opcoes = prompt("Escolha um número de 1 a 5 : ")
//Separando em case
switch (opcoes) {
case "1" :
console.log ("Você escolheu o número 1.")
break;
case "2" :
    console.log ("Você escolheu o número 2 ")
    break
    case "3" :
        console.log ("Você escolheu o número 3 ")
        break
        case "4" :
            console.log ("Você escolheu o número 4")
            break
            case "5" :
                console.log ("Você escolheu o número 5")
                break
                default :
                console.log ("Número não incluso")
                break
}